int fb_init_from_gop(void)
{

    fb_info.phys_addr      = 0xA0000;
    fb_info.width          = 800;
    fb_info.height         = 600;
    fb_info.bpp            = 32;
    fb_info.pixel_format   = FB_PIXEL_FORMAT_ARGB;
    fb_info.bytes_per_line = 800 * 4;

    fb_info.total_size = (uint64_t)fb_info.bytes_per_line * fb_info.height;

    fb_info.total_size = (fb_info.total_size + PAGE_SIZE - 1) & ~(PAGE_SIZE - 1);

    fb_info.addr = (uint8_t*)fb_info.phys_addr;

    framebuffer.buffer = fb_info.addr;
    framebuffer.width  = fb_info.width;
    framebuffer.height = fb_info.height;
    framebuffer.pitch  = fb_info.bytes_per_line;
    framebuffer.bpp     = fb_info.bpp;

    fb_cursor_x = 0;
    fb_cursor_y = 0;

    memset(fb_info.addr, 0, (size_t)(fb_info.bytes_per_line * fb_info.height));

    fb_initialized = 1;

    return 0;
}

fb_info_t* fb_get_info(void)
{
    if (!fb_initialized) {
        return NULL;
    }
    return &fb_info;
}

void fb_set_pixel(uint32_t x, uint32_t y, uint32_t color)
{
    if (!fb_initialized) return;
    if (x >= fb_info.width || y >= fb_info.height) return;

    uint32_t offset = y * fb_info.bytes_per_line + x * 4;
    *(volatile uint32_t*)(fb_info.addr + offset) = color;
}

uint32_t fb_get_pixel(uint32_t x, uint32_t y)
{
    if (!fb_initialized) return 0;
    if (x >= fb_info.width || y >= fb_info.height) return 0;

    uint32_t offset = y * fb_info.bytes_per_line + x * 4;
    return *(volatile uint32_t*)(fb_info.addr + offset);
}

void fb_fill_rect(uint32_t x, uint32_t y, uint32_t w, uint32_t h, uint32_t color)
{
    if (!fb_initialized) return;

    if (x >= fb_info.width || y >= fb_info.height) return;

    if (x + w > fb_info.width) {
        w = fb_info.width - x;
    }
    if (y + h > fb_info.height) {
        h = fb_info.height - y;
    }

    for (uint32_t row = 0; row < h; row++) {
        uint32_t* line = (uint32_t*)(fb_info.addr + (y + row) * fb_info.bytes_per_line + x * 4);
        for (uint32_t col = 0; col < w; col++) {
            line[col] = color;
        }
    }
}

