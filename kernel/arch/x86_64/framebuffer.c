{
    if (!fb_initialized) return;

    int32_t dx = (int32_t)x1 - (int32_t)x0;
    int32_t dy = (int32_t)y1 - (int32_t)y0;

    int32_t sx, sy;

    if (dx > 0) sx = 1;
    else if (dx < 0) { sx = -1; dx = -dx; }
    else sx = 0;

    if (dy > 0) sy = 1;
    else if (dy < 0) { sy = -1; dy = -dy; }
    else sy = 0;

    int32_t err = dx - dy;
    uint32_t cx = x0;
    uint32_t cy = y0;

    for (;;) {
        fb_set_pixel(cx, cy, color);

        if (cx == x1 && cy == y1) break;

        int32_t e2 = 2 * err;

        if (e2 > -dy) {
            err -= dy;
            cx += (uint32_t)sx;
        }
        if (e2 < dx) {
            err += dx;
            cy += (uint32_t)sy;
        }
    }
}

void fb_scroll(uint32_t lines)
{
    if (!fb_initialized) return;
    if (lines == 0) return;

    if (lines >= fb_info.height) {
        fb_clear(fb_bg_color);
        return;
    }

    uint32_t move_bytes = (fb_info.height - lines) * fb_info.bytes_per_line;
    uint32_t clear_bytes = lines * fb_info.bytes_per_line;

    memmove(fb_info.addr, fb_info.addr + clear_bytes, move_bytes);

    uint8_t* clear_start = fb_info.addr + move_bytes;
    for (uint32_t row = 0; row < lines; row++) {
        uint32_t* line = (uint32_t*)(clear_start + row * fb_info.bytes_per_line);
        for (uint32_t col = 0; col < fb_info.width; col++) {
            line[col] = fb_bg_color;
        }
    }
}

void fb_clear(uint32_t color)
{
    if (!fb_initialized) return;

    fb_bg_color = color;

    uint64_t total = (uint64_t)fb_info.width * fb_info.height;
    uint32_t* buf = (uint32_t*)fb_info.addr;

    for (uint64_t i = 0; i < total; i++) {
        buf[i] = color;
    }
}

void fb_set_cursor(uint32_t x, uint32_t y)
{
    if (!fb_initialized) return;

    uint32_t old_px = fb_cursor_x * FB_CHAR_WIDTH;
    uint32_t old_py = fb_cursor_y * FB_CHAR_HEIGHT + FB_CURSOR_SCAN_LINE;
    fb_fill_rect(old_px, old_py, FB_CURSOR_WIDTH, FB_CURSOR_HEIGHT, fb_bg_color);

    fb_cursor_x = x;
    fb_cursor_y = y;

    uint32_t new_px = x * FB_CHAR_WIDTH;
    uint32_t new_py = y * FB_CHAR_HEIGHT + FB_CURSOR_SCAN_LINE;
    fb_fill_rect(new_px, new_py, FB_CURSOR_WIDTH, FB_CURSOR_HEIGHT, FB_COLOR_WHITE);
}

void fb_put_char(uint32_t x, uint32_t y, char ch, uint32_t fg, uint32_t bg)
{
    if (!fb_initialized) return;

    uint8_t index;

    if (ch >= 0x20 && ch <= 0x7E) {
        index = (uint8_t)(ch - 0x20);
    } else {
        index = 0;
    }

    const uint8_t* glyph = font_8x16[index];

    for (uint32_t row = 0; row < FB_CHAR_HEIGHT; row++) {
        uint8_t line = glyph[row];

        for (uint32_t col = 0; col < FB_CHAR_WIDTH; col++) {

            if (line & (0x80 >> col)) {
                fb_set_pixel(x + col, y + row, fg);
            } else {
                fb_set_pixel(x + col, y + row, bg);
            }
        }
    }
}

void fb_puts(uint32_t x, uint32_t y, const char* str, uint32_t fg, uint32_t bg)
{
    if (!fb_initialized) return;
    if (!str) return;

    fb_bg_color = bg;

    uint32_t cur_x = x;
    uint32_t cur_y = y;

    while (*str) {
        if (*str == '\n') {

            cur_x = 0;
            cur_y += FB_CHAR_HEIGHT;

            if (cur_y + FB_CHAR_HEIGHT > fb_info.height) {
                fb_scroll(FB_CHAR_HEIGHT);
                cur_y -= FB_CHAR_HEIGHT;
            }
        } else if (*str == '\r') {

            cur_x = 0;
        } else if (*str == '\t') {

            cur_x = (cur_x + FB_CHAR_WIDTH * 8) & ~(FB_CHAR_WIDTH * 8 - 1);
        } else {

            if (cur_x + FB_CHAR_WIDTH > fb_info.width) {
                cur_x = 0;
                cur_y += FB_CHAR_HEIGHT;

                if (cur_y + FB_CHAR_HEIGHT > fb_info.height) {
                    fb_scroll(FB_CHAR_HEIGHT);
                    cur_y -= FB_CHAR_HEIGHT;
                }
            }

            fb_put_char(cur_x, cur_y, *str, fg, bg);
            cur_x += FB_CHAR_WIDTH;
        }

        str++;
    }

    fb_set_cursor(cur_x / FB_CHAR_WIDTH, cur_y / FB_CHAR_HEIGHT);
}

int fb_resolution_set(uint32_t width, uint32_t height)
{
    if (!fb_initialized) return -1;
    if (width == 0 || height == 0) return -1;

    fb_info.width = width;
    fb_info.height = height;
    fb_info.bytes_per_line = width * (fb_info.bpp / 8);
    fb_info.total_size = (uint64_t)fb_info.bytes_per_line * height;
    fb_info.total_size = (fb_info.total_size + PAGE_SIZE - 1) & ~(PAGE_SIZE - 1);

    framebuffer.width = width;
    framebuffer.height = height;
    framebuffer.pitch = fb_info.bytes_per_line;

    fb_cursor_x = 0;
    fb_cursor_y = 0;

    memset(fb_info.addr, 0, (size_t)(fb_info.bytes_per_line * height));

    return 0;
}

